#include <stdio.h>

void ketugou(char **pt1, char **pt2);

int main(void){
  char str1[] = "Cafe";
  char str2[] = "Takion";

  ketugou(&str1, &str2);
  printf("%s\n", str1);
  
  return 0;
}

void ketugou(char **pt1, char **pt2){
      
    int i;
    int j;
    char *tmp1;
    char *tmp2;

    tmp1 = *pt1;
    for(i=0; tmp1[i]!='\0'; i++){
      tmp2[i]==tmp1[i];
    }
    tmp1 = *pt2;
    
    for(j=0; tmp1[j]!='\0'; j++){
      tmp2[i]==tmp1[j];
      i++;
    }
    *pt1 = tmp2;
    *pt2 = tmp2;
}
